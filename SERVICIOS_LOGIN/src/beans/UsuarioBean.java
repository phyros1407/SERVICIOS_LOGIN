package beans;

public class UsuarioBean {
	
	private String user;
	private String pass;
	private String user_cre;
	private String fec_cre;
	private String user_mod;
	private String fec_mod;
	
	
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getUser_cre() {
		return user_cre;
	}
	public void setUser_cre(String user_cre) {
		this.user_cre = user_cre;
	}
	public String getFec_cre() {
		return fec_cre;
	}
	public void setFec_cre(String fec_cre) {
		this.fec_cre = fec_cre;
	}
	public String getUser_mod() {
		return user_mod;
	}
	public void setUser_mod(String user_mod) {
		this.user_mod = user_mod;
	}
	public String getFec_mod() {
		return fec_mod;
	}
	public void setFec_mod(String fec_mod) {
		this.fec_mod = fec_mod;
	}
	
	
	
	
}
